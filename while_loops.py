x = 0
while x <= 5:
    print(x)
    x = x + 1

# We set the variable x = 0 before we enter the loop.
# We test if x is less than or equal to 5.
# If this condition is true, then we print the value of x.
# With x = x + 1, we increment x by 1.
# The condition is tested again
# We repeat this process until the condition is false.
# When x is greater than 5, the loop stops.
# If you forget to write code inside the loop that makes the test condition false,
# the while loop will continue to run. This is called an infinite loop.